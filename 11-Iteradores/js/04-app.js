// While Loop 
// El while se ejecuta mientras una condicion sea verdadera 

let i = 0; // Inicializar el while 

while(i < 10) { // Condicion

    if ( i % 2 === 0){
        console.log( `Numero ${i} es Par`)
    } else {
        console.log( `Numero ${i} es Impar`)
    }
   

    i++; // Incremento 
}
